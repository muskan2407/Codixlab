package Day_2;

public class ArithmaticOperators {

	
		public static void main(String[] args) {
			  // declare variables
		    int a = 45, b = 88;

		    // addition operator
		    System.out.println("a + b = " + (a + b));

		    // subtraction operator
		    System.out.println("a - b = " + (a - b));

		    // multiplication operator
		    System.out.println("a * b = " + (a * b));

		    // division operator
		    System.out.println("a / b = " + (a / b));

		    // modulo operator
		    System.out.println("a % b = " + (a % b));
		  }

	
	}


